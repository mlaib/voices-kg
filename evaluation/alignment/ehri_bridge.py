#!/usr/bin/env python3
"""Build a VOICES <-> EHRI cross-walk through the shared Wikidata authority.

VOICES already publishes outward ``skos:exactMatch`` links from its minted
``urn:voices:place:<slug>`` IRIs to Wikidata entities. EHRI's controlled
vocabularies (camps, ghettos, places, ...) in turn carry ``seeAlso`` links to
Wikidata. Where a VOICES place and an EHRI concept reference the *same* Wikidata
entity, the two denote the same real-world place and can be bridged.

This script performs that join *live* against the public EHRI Portal API
(no EHRI data is redistributed in this repository -- only the resulting
``skos:closeMatch`` links between IRIs are emitted, exactly as VOICES already
does for GeoNames/Wikidata). For every match it re-fetches the candidate EHRI
concept and confirms the Wikidata QID is actually present in its ``seeAlso``
before emitting a link, so no match rests on a fuzzy keyword hit.

Output: ``schema/voices-ehri-bridge.ttl`` (skos:closeMatch triples).

Usage:
    python evaluation/alignment/ehri_bridge.py \
        --alignment schema/voices-alignment-v2.ttl \
        --out schema/voices-ehri-bridge.ttl
"""
import argparse
import json
import re
import sys
import time
import urllib.parse
import urllib.request

API = "https://portal.ehri-project.eu/api/v1"
PORTAL_KEYWORD = "https://portal.ehri-project.eu/keywords/{}"


def _get(url, retries=3):
    req = urllib.request.Request(
        url, headers={"Accept": "application/json", "User-Agent": "voices-ehri-bridge/1.0"}
    )
    for attempt in range(retries):
        try:
            with urllib.request.urlopen(req, timeout=30) as r:
                return json.load(r)
        except Exception:
            if attempt == retries - 1:
                return None
            time.sleep(1.0 + attempt)
    return None


def read_voices_wikidata(path):
    """Return list of (voices_slug, QID) from the VOICES alignment Turtle."""
    pairs, cur = [], None
    for line in open(path, encoding="utf-8"):
        m = re.search(r"<urn:voices:place:([^>]+)>", line)
        if m:
            cur = m.group(1)
        w = re.search(r"wikidata\.org/entity/(Q\d+)", line)
        if w and cur:
            pairs.append((cur, w.group(1)))
    return pairs


def find_ehri_concept(qid):
    """Search EHRI for a CvocConcept whose seeAlso contains this Wikidata QID.

    Returns (concept_id, prefLabel) on a verified match, else None.
    """
    s = _get(f"{API}/search?q={qid}&type=CvocConcept&limit=10")
    if not s:
        return None
    for d in s.get("data", []):
        cid = d.get("id")
        if not cid:
            continue
        c = _get(f"{API}/{urllib.parse.quote(cid)}")
        if not c:
            continue
        attrs = c.get("data", {}).get("attributes", {})
        see = attrs.get("seeAlso", []) or []
        if any(qid in u for u in see):
            descs = attrs.get("descriptions", [])
            label = descs[0].get("prefLabel") if descs else None
            return cid, label
    return None


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--alignment", default="schema/voices-alignment-v2.ttl")
    ap.add_argument("--out", default="schema/voices-ehri-bridge.ttl")
    ap.add_argument("--limit", type=int, default=0, help="cap QIDs (0 = all; for testing)")
    ap.add_argument("--sleep", type=float, default=0.05)
    args = ap.parse_args()

    pairs = read_voices_wikidata(args.alignment)
    # de-dup by QID, keep first slug
    seen, qid_to_slug = set(), {}
    for slug, qid in pairs:
        if qid not in seen:
            seen.add(qid)
            qid_to_slug[qid] = slug
    qids = list(qid_to_slug)
    if args.limit:
        qids = qids[: args.limit]

    print(f"VOICES Wikidata-aligned places to probe: {len(qids)}", file=sys.stderr)
    matches = []
    for i, qid in enumerate(qids, 1):
        hit = find_ehri_concept(qid)
        if hit:
            cid, label = hit
            matches.append((qid_to_slug[qid], qid, cid, label))
            print(f"  [{i}/{len(qids)}] {qid} -> {cid} ({label})", file=sys.stderr)
        if i % 100 == 0:
            print(f"  ...{i}/{len(qids)} probed, {len(matches)} matches", file=sys.stderr)
        time.sleep(args.sleep)

    with open(args.out, "w", encoding="utf-8") as f:
        f.write("# VOICES <-> EHRI cross-walk (generated by evaluation/alignment/ehri_bridge.py)\n")
        f.write("# Each link connects a VOICES place and an EHRI controlled-vocabulary concept\n")
        f.write("# that reference the SAME Wikidata entity (verified against the EHRI concept's\n")
        f.write("# seeAlso). No EHRI content is redistributed -- only outward SKOS links.\n")
        f.write(f"# Matches: {len(matches)} of {len(qids)} Wikidata-aligned VOICES places.\n")
        f.write("@prefix skos: <http://www.w3.org/2004/02/skos/core#> .\n\n")
        for slug, qid, cid, label in sorted(matches):
            f.write(f"<urn:voices:place:{slug}>\n")
            f.write(f"        skos:closeMatch  <{PORTAL_KEYWORD.format(cid)}> .  # {label} ({qid})\n\n")

    print(f"\nWrote {len(matches)} skos:closeMatch links to {args.out}", file=sys.stderr)


if __name__ == "__main__":
    main()
